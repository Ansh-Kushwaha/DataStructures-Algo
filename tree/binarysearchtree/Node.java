package tree.binarysearchtree;

public class Node {
	public int data;
	public Node left;
	public Node right;
	
	public Node() {
		data = 0;
		left = null;
		right = null;
	}
	
	public Node(int x) {
		data = x;
		left = null;
		right = null;
	}
}
